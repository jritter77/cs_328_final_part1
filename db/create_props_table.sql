drop table props;

create table props(
    seller_name varchar(30),
    seller_phone varchar(12),
    seller_address varchar(50),
    building_name varchar(30),
    building_price number,
    building_address varchar(50),
    area varchar(20),
    type varchar(20),
    sqft number,
    height number,
    rooms number,
    floors number,
    year_built number
);